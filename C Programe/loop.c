#include <stdio.h>

int main(void)
{
	int i = 0;
	while (i < 3)
	{
		printf("meow \n");
		i ++;
	}	
	printf("--------------------------------\n");

	int j = 3;
	while (j > 0)
	{
		printf("meow \n");
		j --;
	}	
printf("--------------------------------\n");

	for (int k = 0; k < 3; k++ )
	{
		printf("meow \n");
		k ++;
	}
}